f = open("input.txt","r")
for i in f:
    str1 = i
#print(str1)
alphabet = "abcdefghijklmnopqrstuvwxyz"
done = False
while not done:
    previous = str1
    for i in alphabet:
        str1 = str1.replace(i + i.upper(), "")
        str1 = str1.replace(i.upper() + i, "")
    if str1 == previous:
        done = True
print(len(str1))



























































































































































































